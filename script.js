function showQuotes () {
    let text = document.getElementById('text');
    let selectQuote = Math.floor(Math.random() * quotes.length);
    text.innerHTML = quotes[selectQuote];
}

function randomImage () {
    let selectImage = Math.floor(Math.random() * images.length);
    let image = document.getElementById('image');
    image.src = images[selectImage];
}

function rotateImage () {
    let numberOfDegs = Math.round(Math.random() * 100) / 25;
    let bothDegs = numberOfDegs * Math.round(Math.random()) * 2 - 1
    let string = 'rotate(' + bothDegs + 'deg)'
    document.getElementById("image").style.transform = string;
}

showQuotes();
randomImage();
rotateImage();

let time = 8000;

window.setInterval(showQuotes,time);
window.setInterval(rotateImage,time);
